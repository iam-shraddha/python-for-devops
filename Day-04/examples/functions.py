#Program for calculator using function

num1 = 20
num2 = 10

def addition():
  add = num1 + num2
  print("Addition of two numbers: " + str(add))

def subtraction():
  sub = num1 + num2
  print("Subtraction of two numbers: " + str(sub))

def multiplication():
  mul = num1 * num2
  print("Multition of two numbers: " + str(mul))

def division():
  div = num1 / num2
  print("Division of two numbers: " + str(div))

addition()
subtraction()
multiplication()
division()
